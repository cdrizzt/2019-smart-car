void  Lcd_WriteIndex(uint8 dat)			//写命令
{	
    DC(0);
    spi_mosi(spi1,SPI_PCS1,&dat,&dat,1);		

}

void Lcd_WriteData(uint8 dat)			//写数据
{
    DC(1);
    spi_mosi(spi1,SPI_PCS1,&dat,&dat,1);
}

void  LCD_WriteData_16Bit(uint16 dat)	//向液晶屏写一个16位数据
{
    uint8 h,l;
    h = dat >> 8;
    l = (uint8)dat;
    
    DC(1);
    spi_mosi(spi1,SPI_PCS1,&h,&h,1); 	//写入高8位数据
    spi_mosi(spi1,SPI_PCS1,&l,&l,1);	//写入低8位数据
}

